package com.spring.project.activity.dto;

public class ActivityDTO {

	private int act_no;
	private String act_name;
	private String act_address;
	private int act_price;
	
	
	public int getAct_no() {
		return act_no;
	}
	public void setAct_no(int act_no) {
		this.act_no = act_no;
	}
	public String getAct_name() {
		return act_name;
	}
	public void setAct_name(String act_name) {
		this.act_name = act_name;
	}
	public String getAct_address() {
		return act_address;
	}
	public void setAct_address(String act_address) {
		this.act_address = act_address;
	}
	public int getAct_price() {
		return act_price;
	}
	public void setAct_price(int act_price) {
		this.act_price = act_price;
	}
	
	
	
}
